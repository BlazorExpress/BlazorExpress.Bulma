namespace BlazorExpress.Bulma;

/// <summary>
/// Represents the size of a Bootstrap Icon.
///     <para><see href="https://bulma.io/documentation/helpers/typography-helpers/#size" /></para>
/// </summary>
public enum BootstrapIconSize
{
    /// <summary>
    /// Represents `is-size-4`.
    /// </summary>
    None,

    /// <summary>
    /// Represents `is-size-7`.
    /// </summary>
    XSmall,

    /// <summary>
    /// Represents `is-size-6`.
    /// </summary>
    Small,

    /// <summary>
    /// Represents `is-size-5`.
    /// </summary>
    Medium,

    /// <summary>
    /// Represents `is-size-3`.
    /// </summary>
    Large,

    /// <summary>
    /// Represents `is-size-2`.
    /// </summary>
    XLarge,

    /// <summary>
    /// Represents `is-size-1`.
    /// </summary>
    XXLarge
}
