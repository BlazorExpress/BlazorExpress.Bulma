namespace BlazorExpress.Bulma;

/// <summary>
/// Represents the ratio of an image.
/// <para>
///     <see href="https://bulma.io/documentation/elements/image/#responsive-images-with-ratios" />
/// </para>
/// </summary>
public enum ImageRatio
{
    None,

    /// <summary>
    /// Square (or 1 by 1)
    /// </summary>
    IsSquare,

    /// <summary>
    /// 1 by 1
    /// </summary>
    Is1by1,

    /// <summary>
    /// 1 by 2
    /// </summary>
    Is1by2,

    /// <summary>
    /// 1 by 3
    /// </summary>
    Is1by3,

    /// <summary>
    /// 2 by 1
    /// </summary>
    Is2by1,

    /// <summary>
    /// 2 by 3
    /// </summary>
    Is2by3,

    /// <summary>
    /// 3 by 1
    /// </summary>
    Is3by1,

    /// <summary>
    /// 3 by 2
    /// </summary>
    Is3by2,

    /// <summary>
    /// 3 by 4
    /// </summary>
    Is3by4,

    /// <summary>
    /// 3 by 5
    /// </summary>
    Is3by5,

    /// <summary>
    /// 4 by 3
    /// </summary>
    Is4by3,

    /// <summary>
    /// 4 by 5
    /// </summary>
    Is4by5,

    /// <summary>
    /// 5 by 3
    /// </summary>
    Is5by3,

    /// <summary>
    /// 5 by 4
    /// </summary>
    Is5by4,

    /// <summary>
    /// 9 by 16
    /// </summary>
    Is9by16,

    /// <summary>
    /// 16 by 9
    /// </summary>
    Is16by9
}
