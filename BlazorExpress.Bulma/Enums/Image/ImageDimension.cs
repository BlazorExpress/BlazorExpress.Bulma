namespace BlazorExpress.Bulma;

/// <summary>
/// Represents the dimensions of an image.
/// <para>
///     <see href="https://bulma.io/documentation/elements/image/#fixed-square-images" />
/// </para>
/// </summary>
public enum ImageDimension
{
    None,

    /// <summary>
    /// 16x16px
    /// </summary>
    Is16x16,

    /// <summary>
    /// 24x24px
    /// </summary>
    Is24x24,

    /// <summary>
    /// 32x32px
    /// </summary>
    Is32x32,

    /// <summary>
    /// 48x48px
    /// </summary>
    Is48x48,

    /// <summary>
    /// 64x64px
    /// </summary>
    Is64x64,

    /// <summary>
    /// 96x96px
    /// </summary>
    Is96x96,

    /// <summary>
    /// 128x128px
    /// </summary>
    Is128x128
}
