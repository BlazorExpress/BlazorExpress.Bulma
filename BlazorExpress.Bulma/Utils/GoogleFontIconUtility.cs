    /// <summary>
    /// Returns the CSS class for the specified icon.
    /// </summary>
    /// <param name="style"></param>
    /// <returns>string</returns>
    public static string Icon(GoogleFontIconStyle style)
    {
        return $"{Icon()}-{ToIconStyleName(style)}";
    }

